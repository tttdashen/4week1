abs#函数本身
x=abs(-10)#赋值给变量
print(x)
f=abs#把函数本身赋值给变量
print(f)#结论：函数本身也可以赋值给变量，即：变量可以指向函数。
print(f(-10))#成功！说明变量f现在已经指向了abs函数本身。直接调用abs()函数和调用变量f()完全相同
