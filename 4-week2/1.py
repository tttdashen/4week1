#上述讨论了变量可以指向函数，函数的参数可以接收变量，那么一个函数就可以接收另一个函数作为参数，这种函数就称之为高阶函数

def add(x,y,f):
    return f(x)+f(y)
x=add(1,-10,abs)#函数嵌套函数
print(x)
#编写高阶函数，就是让函数的参数能够接收别的函数。